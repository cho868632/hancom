a = 5
b = 2

print(a + b)   # 7    덧셈
print(a - b)   # 3    뺄셈
print(a * b)   # 10   곱셈
print(a / b)   # 2.5  나눗셈
print(a // b)  # 2    몫
print(a % b)   # 1    나머지
print(a ** b)  # 25   거듭제곱
a = "hello"
b = "world"
print(a + b)   # helloworld  (이어붙이기)
print(a * 2)   # hellohello  (반복)
